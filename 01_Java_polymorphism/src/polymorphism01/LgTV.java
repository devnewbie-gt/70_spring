package polymorphism01;

public class LgTV {
	public void on() {
		System.out.println("LgTV - 전원 ON");
	}
	public void off() {
		System.out.println("LgTV - 전원 OFF");
	}
	public void soundUp() {
		System.out.println("LgTV - 볼륨 UP");
	}
	public void soundDown() {
		System.out.println("LgTV - 볼륨 DOWN");
	}
}
